<template>
	<div class="container p-4">
		<div class="row justify-content-center">
			<div class="col-md-6">
				<div class="card">
					<div class="card-header">Login</div>
					<div class="card-body">
                        <div class="form-group row">
                            <label for="email" class="col-md-4 col-form-label text-md-right">Email</label>
                            <div class="col-md-6">
                                <input id="email" type="email" class="form-control " v-model="cred.email"  >
                            </div>
                        </div>
                        <div class="form-group row">
                            <label for="password" class="col-md-4 col-form-label text-md-right">Password</label>
                            <div class="col-md-6">
                                <input id="password" type="password" class="form-control" v-model="cred.password" >
                            </div>
                        </div>
                        <div class="form-group row mb-0">
                            <div class="col-md-8 offset-md-4">
                                <button type="submit" @click="submit" class="btn btn-secondary">Login</button>
                            </div>
                        </div>
					</div>
				</div>
			</div>
		</div>
	</div>
</template>

<script>
    import auth from '@/auth'
    export default {
        data(){
            return{
                cred:{
                    email:'',
                    password:''
                }
            }
        },
        methods:{
            async submit(){
                const res = await auth.LogIn(this.cred);
                if (res.error !==true) this.$router.push('/tab1');
            }
        }
    }
</script>
